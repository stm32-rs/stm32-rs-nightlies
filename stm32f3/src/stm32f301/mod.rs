#[no_mangle]
static mut DEVICE_PERIPHERALS: bool = false;
/// All the peripherals.
#[allow(non_snake_case)]
pub struct Peripherals {
    ///GPIOA
    pub GPIOA: GPIOA,
    ///GPIOB
    pub GPIOB: GPIOB,
    ///GPIOC
    pub GPIOC: GPIOC,
    ///GPIOD
    pub GPIOD: GPIOD,
    ///GPIOF
    pub GPIOF: GPIOF,
    ///TSC
    pub TSC: TSC,
    ///CRC
    pub CRC: CRC,
    ///FLASH
    pub FLASH: FLASH,
    ///RCC
    pub RCC: RCC,
    ///DMA1
    pub DMA1: DMA1,
    ///TIM2
    pub TIM2: TIM2,
    ///TIM15
    pub TIM15: TIM15,
    ///TIM16
    pub TIM16: TIM16,
    ///TIM17
    pub TIM17: TIM17,
    ///USART1
    pub USART1: USART1,
    ///USART2
    pub USART2: USART2,
    ///USART3
    pub USART3: USART3,
    ///SPI2
    pub SPI2: SPI2,
    ///SPI3
    pub SPI3: SPI3,
    ///I2S2ext
    pub I2S2EXT: I2S2EXT,
    ///I2S3ext
    pub I2S3EXT: I2S3EXT,
    ///ADC1
    pub ADC1: ADC1,
    ///PWR
    pub PWR: PWR,
    ///I2C1
    pub I2C1: I2C1,
    ///I2C2
    pub I2C2: I2C2,
    ///I2C3
    pub I2C3: I2C3,
    ///IWDG
    pub IWDG: IWDG,
    ///WWDG
    pub WWDG: WWDG,
    ///RTC
    pub RTC: RTC,
    ///TIM6
    pub TIM6: TIM6,
    ///DAC1
    pub DAC1: DAC1,
    ///DBGMCU
    pub DBGMCU: DBGMCU,
    ///SYSCFG
    pub SYSCFG: SYSCFG,
    ///OPAMP
    pub OPAMP: OPAMP,
    ///COMP
    pub COMP: COMP,
    ///TIM1
    pub TIM1: TIM1,
    ///ADC1_2
    pub ADC1_2: ADC1_2,
    ///EXTI
    pub EXTI: EXTI,
}
impl Peripherals {
    /// Returns all the peripherals *once*.
    #[cfg(feature = "critical-section")]
    #[inline]
    pub fn take() -> Option<Self> {
        critical_section::with(|_| {
            if unsafe { DEVICE_PERIPHERALS } {
                return None;
            }
            Some(unsafe { Peripherals::steal() })
        })
    }
    /// Unchecked version of `Peripherals::take`.
    ///
    /// # Safety
    ///
    /// Each of the returned peripherals must be used at most once.
    #[inline]
    pub unsafe fn steal() -> Self {
        DEVICE_PERIPHERALS = true;
        Peripherals {
            GPIOA: GPIOA::steal(),
            GPIOB: GPIOB::steal(),
            GPIOC: GPIOC::steal(),
            GPIOD: GPIOD::steal(),
            GPIOF: GPIOF::steal(),
            TSC: TSC::steal(),
            CRC: CRC::steal(),
            FLASH: FLASH::steal(),
            RCC: RCC::steal(),
            DMA1: DMA1::steal(),
            TIM2: TIM2::steal(),
            TIM15: TIM15::steal(),
            TIM16: TIM16::steal(),
            TIM17: TIM17::steal(),
            USART1: USART1::steal(),
            USART2: USART2::steal(),
            USART3: USART3::steal(),
            SPI2: SPI2::steal(),
            SPI3: SPI3::steal(),
            I2S2EXT: I2S2EXT::steal(),
            I2S3EXT: I2S3EXT::steal(),
            ADC1: ADC1::steal(),
            PWR: PWR::steal(),
            I2C1: I2C1::steal(),
            I2C2: I2C2::steal(),
            I2C3: I2C3::steal(),
            IWDG: IWDG::steal(),
            WWDG: WWDG::steal(),
            RTC: RTC::steal(),
            TIM6: TIM6::steal(),
            DAC1: DAC1::steal(),
            DBGMCU: DBGMCU::steal(),
            SYSCFG: SYSCFG::steal(),
            OPAMP: OPAMP::steal(),
            COMP: COMP::steal(),
            TIM1: TIM1::steal(),
            ADC1_2: ADC1_2::steal(),
            EXTI: EXTI::steal(),
        }
    }
}
