#[no_mangle]
static mut DEVICE_PERIPHERALS: bool = false;
/// All the peripherals.
#[allow(non_snake_case)]
pub struct Peripherals {
    ///ADC_Common
    pub ADC_COMMON: ADC_COMMON,
    ///ADC1
    pub ADC1: ADC1,
    ///CRC
    pub CRC: CRC,
    ///DBGMCU
    pub DBGMCU: DBGMCU,
    ///EXTI
    pub EXTI: EXTI,
    ///FLASH
    pub FLASH: FLASH,
    ///IWDG
    pub IWDG: IWDG,
    ///PWR
    pub PWR: PWR,
    ///RCC
    pub RCC: RCC,
    ///RTC
    pub RTC: RTC,
    ///SYSCFG
    pub SYSCFG: SYSCFG,
    ///TIM1
    pub TIM1: TIM1,
    ///TIM8
    pub TIM8: TIM8,
    ///TIM11
    pub TIM11: TIM11,
    ///TIM5
    pub TIM5: TIM5,
    ///TIM9
    pub TIM9: TIM9,
    ///USART1
    pub USART1: USART1,
    ///USART2
    pub USART2: USART2,
    ///USART6
    pub USART6: USART6,
    ///WWDG
    pub WWDG: WWDG,
    ///DMA1
    pub DMA1: DMA1,
    ///DMA2
    pub DMA2: DMA2,
    ///GPIOH
    pub GPIOH: GPIOH,
    ///GPIOC
    pub GPIOC: GPIOC,
    ///GPIOB
    pub GPIOB: GPIOB,
    ///GPIOA
    pub GPIOA: GPIOA,
    ///I2C1
    pub I2C1: I2C1,
    ///I2C2
    pub I2C2: I2C2,
    ///SPI1
    pub SPI1: SPI1,
    ///SPI2
    pub SPI2: SPI2,
    ///SPI5
    pub SPI5: SPI5,
    ///TIM6
    pub TIM6: TIM6,
    ///RNG
    pub RNG: RNG,
    ///DAC
    pub DAC: DAC,
    ///LPTIM1
    pub LPTIM1: LPTIM1,
    ///FMPI2C1
    pub FMPI2C1: FMPI2C1,
}
impl Peripherals {
    /// Returns all the peripherals *once*.
    #[cfg(feature = "critical-section")]
    #[inline]
    pub fn take() -> Option<Self> {
        critical_section::with(|_| {
            if unsafe { DEVICE_PERIPHERALS } {
                return None;
            }
            Some(unsafe { Peripherals::steal() })
        })
    }
    /// Unchecked version of `Peripherals::take`.
    ///
    /// # Safety
    ///
    /// Each of the returned peripherals must be used at most once.
    #[inline]
    pub unsafe fn steal() -> Self {
        DEVICE_PERIPHERALS = true;
        Peripherals {
            ADC_COMMON: ADC_COMMON::steal(),
            ADC1: ADC1::steal(),
            CRC: CRC::steal(),
            DBGMCU: DBGMCU::steal(),
            EXTI: EXTI::steal(),
            FLASH: FLASH::steal(),
            IWDG: IWDG::steal(),
            PWR: PWR::steal(),
            RCC: RCC::steal(),
            RTC: RTC::steal(),
            SYSCFG: SYSCFG::steal(),
            TIM1: TIM1::steal(),
            TIM8: TIM8::steal(),
            TIM11: TIM11::steal(),
            TIM5: TIM5::steal(),
            TIM9: TIM9::steal(),
            USART1: USART1::steal(),
            USART2: USART2::steal(),
            USART6: USART6::steal(),
            WWDG: WWDG::steal(),
            DMA1: DMA1::steal(),
            DMA2: DMA2::steal(),
            GPIOH: GPIOH::steal(),
            GPIOC: GPIOC::steal(),
            GPIOB: GPIOB::steal(),
            GPIOA: GPIOA::steal(),
            I2C1: I2C1::steal(),
            I2C2: I2C2::steal(),
            SPI1: SPI1::steal(),
            SPI2: SPI2::steal(),
            SPI5: SPI5::steal(),
            TIM6: TIM6::steal(),
            RNG: RNG::steal(),
            DAC: DAC::steal(),
            LPTIM1: LPTIM1::steal(),
            FMPI2C1: FMPI2C1::steal(),
        }
    }
}
